// Boolians
true
false

// Comparison Operators
3 > 2;
2>3;
1<2;
2<1;

// Equality Operators
2 == 2;
2 == 3;
"hello" == "bye";

// This below will come back as true because it is comparing the value not the type
"2" == 2;
// This below will come back as false because it is comparing the value and the type, use === instead of ==
"2" === 2;
"2" === "2";

// Inequality Operators
// adding the extra = will check the value and the type
"2" !== 2;
// below only checks for value, not type
"2" != 2;

// Boolean 1 = true, 0 = false
// below will come back as true because 1 is true and is not checking for data type
true == 1;
// below will come back as false because it is checking for data type
true === 1;

// logical operators

// && = and
1 === 1 && 2 === 2;
1===1 && 2===2 && 1===2;

// || = or
1 === 1 || 2 === 2;
1===1 || 2===2 || 1===2;

// ! = not
(1===1);
!(1===1);
!(1===2);
// You can stack NOT operators
!!(1===1);
!!!!!(1===1);






